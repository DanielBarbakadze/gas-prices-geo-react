const isGasOrLPGPricesData = (label) =>
  label.includes("LPG") || label.includes("CNG");

export const socarDates = Array.from(
  Object.values(socarPricesHistory)[0].slice(1)
).map((date) => getMonthNameAndDayFromDate(date));

export const socarPricesData = Object.values(socarPricesHistory)
  .slice(1)
  .filter((item) => (isGasOrLPGPricesData(item[0]) ? null : item))
  .map((item) => item.slice(1))
  .filter((item) => !item.every((price) => price === "0.00"));

export const socarTitles = Object.values(socarPricesHistory)
  .slice(1)
  .map((item) => item[0]);

export const gasAndLPGPricesData = Object.values(socarPricesHistory)
  .slice(1)
  .filter((item) => (isGasOrLPGPricesData(item[0]) ? item : null))
  .map((item) => item.slice(1));

